package day02;

public class FourArithmetic {
    public static void main(String[] args) {
        int a = 12;
        int b = 2;
        int result = 0;

        result = a + b;
        System.out.printf("a+b=%d%n", result);
        result = a - b;
        System.out.printf("a-b=%d%n", result);
        result = a * b;
        System.out.printf("a*b=%d%n", result);
        result = a / b;
        System.out.printf("a/b=%d%n", result);
    }
}
